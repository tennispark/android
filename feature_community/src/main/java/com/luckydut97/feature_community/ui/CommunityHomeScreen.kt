    onSearchClick: () -> Unit = {},
    onAlarmClick: () -> Unit = {},
    onWriteClick: () -> Unit = {},
    viewModel: CommunityHomeViewModel,
    onEditPost: (CommunityPost) -> Unit = {},
    onDeletePost: (CommunityPost) -> Unit = {}
) {
    val uiState by viewModel.uiState.collectAsState()
    val listState = rememberLazyListState()
    val pullToRefreshState = rememberPullToRefreshState()
    val indicatorColor = Color(0xFF1C7756)
    var pendingDeletePost by remember { mutableStateOf<CommunityPost?>(null) }

    // Î¨¥Ìïú Ïä§ÌÅ¨Î°§: Î¶¨Ïä§Ìä∏ ÎÅù Í∞êÏßÄ
    val shouldLoadMore = remember {
        derivedStateOf {
            val layoutInfo = listState.layoutInfo
            val totalItemsNumber = layoutInfo.totalItemsCount
            val lastVisibleItemIndex = (layoutInfo.visibleItemsInfo.lastOrNull()?.index ?: 0) + 1

            // ÎîîÎ≤ÑÍπÖ Î°úÍ∑∏
            println("üîÑ Î¨¥ÌïúÏä§ÌÅ¨Î°§ Ï≤¥ÌÅ¨:")
            println("   - Ï†ÑÏ≤¥ ÏïÑÏù¥ÌÖú Ïàò: $totalItemsNumber")
            println("   - ÎßàÏßÄÎßâ Î≥¥Ïù¥Îäî Ïù∏Îç±Ïä§: $lastVisibleItemIndex")
            println("   - Ìä∏Î¶¨Í±∞ Í∏∞Ï§Ä: ${totalItemsNumber - 3}")
            println("   - canLoadMore: ${uiState.canLoadMore}")
            println("   - isLoadingMore: ${uiState.isLoadingMore}")

            // ÎßàÏßÄÎßâÏóêÏÑú 3Í∞ú Ï†Ñ ÏïÑÏù¥ÌÖúÏù¥ Î≥¥Ïùº Îïå Îã§Ïùå ÌéòÏù¥ÏßÄ Î°úÎìú
            val shouldTrigger = lastVisibleItemIndex > (totalItemsNumber - 3) &&
                    totalItemsNumber > 0 &&
                    uiState.canLoadMore &&
                    !uiState.isLoadingMore

            if (shouldTrigger) {
                println("‚úÖ Îã§Ïùå ÌéòÏù¥ÏßÄ Î°úÎìú Ìä∏Î¶¨Í±∞!")
            }

            shouldTrigger
        }
    }

    LaunchedEffect(shouldLoadMore.value) {
        if (shouldLoadMore.value) {
            viewModel.loadNextPage()
        }
    }

    Box(
        modifier = Modifier
            .fillMaxSize()
            .background(Color.White)
    ) {
        Column(
            modifier = Modifier.fillMaxSize()
        ) {
            // ÏÉÅÎã®Î∞î
            CommunityTopBar(
                onSearchClick = onSearchClick,
                onAlarmClick = onAlarmClick
            )

            // Pull-to-Refresh BoxÎ°ú Î©îÏù∏ ÏΩòÌÖêÏ∏† Í∞êÏã∏Í∏∞
            PullToRefreshBox(
                isRefreshing = uiState.isRefreshing,
                onRefresh = { viewModel.refresh() },
                modifier = Modifier.fillMaxSize(),
                state = pullToRefreshState,
                indicator = {
                    PullToRefreshDefaults.Indicator(
                        state = pullToRefreshState,
                        isRefreshing = uiState.isRefreshing,
                        modifier = Modifier.align(Alignment.TopCenter),
                        color = Color.White,
                        containerColor = indicatorColor
                    )
                }
            ) {
                when {
                    uiState.isLoading && uiState.posts.isEmpty() -> {
                        Box(
                            modifier = Modifier.fillMaxSize(),
                            contentAlignment = Alignment.Center
                        ) {
                            CircularProgressIndicator(
                                color = Color(0xFF5AB97D)
                            )
                        }
                    }

                    uiState.errorMessage != null && uiState.posts.isEmpty() -> {
                        Box(
                            modifier = Modifier.fillMaxSize(),
                            contentAlignment = Alignment.Center
                        ) {
                            Column(
                                horizontalAlignment = Alignment.CenterHorizontally,
                                modifier = Modifier.padding(horizontal = 24.dp)
                            ) {
                                Text(
                                    text = uiState.errorMessage ?: "Ïò§Î•òÍ∞Ä Î∞úÏÉùÌñàÏäµÎãàÎã§",
                                    color = Color.Gray
                                )
                                Spacer(modifier = Modifier.height(16.dp))
                                Button(
                                    onClick = {
                                        viewModel.clearError()
                                        viewModel.loadCommunityPosts()
                                    },
                                    colors = ButtonDefaults.buttonColors(
                                        containerColor = Color(0xFF5AB97D)
                                    )
                                ) {
                                    Text("Îã§Ïãú ÏãúÎèÑ")
                                }
                            }
                        }
                    }

                    uiState.posts.isEmpty() && !uiState.isLoading -> {
                        Box(
                            modifier = Modifier.fillMaxSize(),
                            contentAlignment = Alignment.Center
                        ) {
                            Column(
                                horizontalAlignment = Alignment.CenterHorizontally,
                                modifier = Modifier.padding(horizontal = 24.dp)
                            ) {
                                Text(
                                    text = "Í≤åÏãúÍ∏ÄÏù¥ ÏóÜÏäµÎãàÎã§",
                                    color = Color.Gray
                                )
                                Spacer(modifier = Modifier.height(8.dp))
                                Text(
                                    text = "Ï≤´ Î≤àÏß∏ Í≤åÏãúÍ∏ÄÏùÑ ÏûëÏÑ±Ìï¥Î≥¥ÏÑ∏Ïöî.",
                                    color = Color.LightGray
                                )
                            }
                        }
                    }

                    else -> {
                        LazyColumn(
                            modifier = Modifier.fillMaxSize(),
                            state = listState
                        ) {
                            items(uiState.posts) { post ->
                                CommunityPostCard(
                                    post = post,
                                    onPostClick = { onPostClick(post.id) },
                                    onLikeClick = { viewModel.toggleLike(post.id) },
                                    onCommentClick = { onPostClick(post.id) },
                                    onAlarmClick = onAlarmClick,
                                    onEditClick = onEditPost,
                                    onDeleteClick = { pendingDeletePost = it }
                                )

                                // Í≤åÏãúÍ∏Ä Íµ¨Î∂ÑÏÑ†
                                Spacer(
                                    modifier = Modifier
                                        .height(12.dp)
                                        .fillMaxWidth()
                                        .background(Color(0xFFF5F5F5))
                                )
                            }

                            // Ï∂îÍ∞Ä Î°úÎî© Ïù∏ÎîîÏºÄÏù¥ÌÑ∞
                            if (uiState.isLoadingMore) {
                                item {
                                    Box(
                                        modifier = Modifier
                                            .fillMaxWidth()
                                            .padding(16.dp),
                                        contentAlignment = Alignment.Center
                                    ) {
                                        CircularProgressIndicator(
                                            color = Color(0xFF5AB97D),
                                            modifier = Modifier.size(24.dp)
                                        )
                                    }
                                }
                            }

                            // ÎßàÏßÄÎßâ ÌéòÏù¥ÏßÄ ÌëúÏãú
                            if (!uiState.hasNextPage && uiState.posts.isNotEmpty()) {
                                item {
                                    Box(
                                        modifier = Modifier
                                            .fillMaxWidth()
                                            .padding(1.dp),
                                        contentAlignment = Alignment.Center
                                    ) {
                                        Text(
                                            text = "Î™®Îì† Í≤åÏãúÍ∏ÄÏùÑ Î∂àÎü¨ÏôîÏäµÎãàÎã§",
                                            color = Color.Gray
                                        )
                                    }
                                }
                            }

                            // ÌïòÎã® Ïó¨Î∞± (ÌîåÎ°úÌåÖ Î≤ÑÌäº Í≥µÍ∞Ñ ÌôïÎ≥¥)
                            item {
                                Spacer(modifier = Modifier.height(30.dp))
                            }
                        }
                    }
                }
            }
        }

        // ÌîåÎ°úÌåÖ Í∏ÄÏì∞Í∏∞ Î≤ÑÌäº (Î∞îÌÖÄÎÑ§ÎπÑ 18dp ÏúÑ, Ïö∞Ï∏° 18dp Ïó¨Î∞±)
        FloatingWriteButton(
            onClick = onWriteClick,
            modifier = Modifier
                .align(Alignment.BottomEnd)
                .padding(end = 18.dp, bottom = 18.dp)
        )
    }

    pendingDeletePost?.let { post ->
        ConfirmDeleteDialog(
            onConfirm = {
                onDeletePost(post)
                pendingDeletePost = null
            },
            onDismiss = { pendingDeletePost = null }
        )
    }
}
